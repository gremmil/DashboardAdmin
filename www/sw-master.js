self.addEventListener("sync", (e) => {
  let respuesta;
  const key = e.tag.split('sync')[1]
  const storageSync = e.tag.split('sync')[0]
  switch( storageSync ){

    case swSchema.storages.listaDescripcionPedidos :
        respuesta = enviarData( storageSync, key );
    break;
    case swSchema.storages.subidaImagenes :
      respuesta = enviarData( storageSync, key );
      break;
    default:
    break;
  }

    e.waitUntil(respuesta);

});
